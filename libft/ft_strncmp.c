#include "libft.h"
/* Compares up to 'n' characters from strings 's1' and 's2' */
int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned int	i;

	i = 0;
	while (i < n && s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			return ((s1[i] - s2[i]));
		}
		i++;
	}
	if (i != n)
		return (s1[i] - s2[i]);
	return (0);
}
