#include "libft.h"
/* Delete the "lst" node, calling the "del" function 
to free the node memory. 
Does not consider relink from the list */
void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	free(lst);
}
