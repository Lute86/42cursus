#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	ft_a;
	int	ft_b;

	ft_a = *a / *b;
	ft_b = *a % *b;
	*a = ft_a;
	*b = ft_b;
}
